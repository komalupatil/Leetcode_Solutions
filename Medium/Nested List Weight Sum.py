class Solution:
    def depthSum(self, nestedList: List[NestedInteger]) -> int:
        if not nestedList:
            return 0
        ans = 0
        depth = 1
        queue = deque()
        
        for i in nestedList:
            queue.append(i)
            
        while queue:
            
            levelSize = len(queue)
            for _ in range(levelSize):
                val = queue.popleft()
                if val.isInteger():
                    ans += depth*val.getInteger()
                else:
                    for i in val.getList():
                        queue.append(i)
            depth += 1
        return ans